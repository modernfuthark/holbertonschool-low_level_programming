#include "3-calc.h"
/**
 * op_add - Add A B
 * @a: Number 1
 * @b: Number 2
 * Return: A added to B
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Subtract A B
 * @a: Number 1
 * @b: Number 2
 * Return: A subtraced by B
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiply A B
 * @a: Number 1
 * @b: Number 2
 * Return: A multiplied by B
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Divide A B
 * @a: Number 1
 * @b: Number 2
 * Return: A divided by B
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Mod A B
 * @a: Number 1
 * @b: Number 2
 * Return: A mod B
 */

int op_mod(int a, int b)
{
	return (a % b);
}
